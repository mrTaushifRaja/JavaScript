//comparison operators
let a=5;   //number
let b="6"; //string
// not equal to (!=)
console.log("a==b",a!=b);  //true

let x=4;   //number
let y=5; //number
//equal to
console.log("a==b",x==y);

let f=4;   //number
let g=5; //number
//equal to & type
console.log("f===g",f===g);

let h=4;   //number
let i=5; //number
// not equal to & type
console.log("h===i",h===i);


//other comparison operators
// >, >= ,< ,   <= 
console.log("5>3",a>b);
console.log("5<=3",a<=b);
console.log("5>=3",a>=b);
console.log("5<3",a<b);