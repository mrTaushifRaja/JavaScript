//Assignment Operators
let a=5;
let b=4;
console.log("a =",a,"b =",b);
console.log("a =",a+=3);
console.log("b =",b+=4);

console.log("a =",a-=3);
console.log("b =",b-=4);

console.log("a =",a*=3);
console.log("b =",b*=4);

console.log("a =",a/=3);
console.log("b =",b/=4);

console.log("a =",a%=3);
console.log("b =",b%=4);

console.log("a =",a**=3);
console.log("b =",a**=4);
