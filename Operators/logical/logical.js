let a=5;
let b=4;
//logical AND
let cond1 = a>b; //true
let cond2 = a===5; //true
console.log("cond1 && cond2 =",cond1 && cond2);

let x=5;
let y=4;
//logical OR
let cond3 = x>y; //true
let cond4 = x<y; //true
console.log("cond3 || cond4 =",x<y||x===y);

let c=5;
let d=4;
//logical Not
let cond5 = c>d; //true
let cond6 = c<d; //true
console.log("cond5! cond6 =",!(c<d));

